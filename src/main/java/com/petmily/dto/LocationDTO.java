package com.petmily.dto;

public class LocationDTO {
	private String sidoName;
	private int sidoCode;
	private String sigundoName;
	private int sigundoCode;
	
	
	public String getSidoName() {
		return sidoName;
	}
	public void setSidoName(String sidoName) {
		this.sidoName = sidoName;
	}
	public int getSidoCode() {
		return sidoCode;
	}
	public void setSidoCode(int sidoCode) {
		this.sidoCode = sidoCode;
	}
	public String getSigundoName() {
		return sigundoName;
	}
	public void setSigundoName(String sigundoName) {
		this.sigundoName = sigundoName;
	}
	public int getSigundoCode() {
		return sigundoCode;
	}
	public void setSigundoCode(int sigundoCode) {
		this.sigundoCode = sigundoCode;
	}
	
	
}
