package com.petmily.dto;

public class QuizDTO {
	private int quiz_idx;
	private String quiz_category;
	private String animal_idx;
	private String quiz_ask;
	private String quiz_content;
	private String quiz_answer;
	
	public int getQuiz_idx() {
		return quiz_idx;
	}
	public void setQuiz_idx(int quiz_idx) {
		this.quiz_idx = quiz_idx;
	}
	public String getQuiz_category() {
		return quiz_category;
	}
	public void setQuiz_category(String quiz_category) {
		this.quiz_category = quiz_category;
	}
	public String getAnimal_idx() {
		return animal_idx;
	}
	public void setAnimal_idx(String animal_idx) {
		this.animal_idx = animal_idx;
	}
	public String getQuiz_ask() {
		return quiz_ask;
	}
	public void setQuiz_ask(String quiz_ask) {
		this.quiz_ask = quiz_ask;
	}
	public String getQuiz_content() {
		return quiz_content;
	}
	public void setQuiz_content(String quiz_content) {
		this.quiz_content = quiz_content;
	}
	public String getQuiz_answer() {
		return quiz_answer;
	}
	public void setQuiz_answer(String quiz_answer) {
		this.quiz_answer = quiz_answer;
	}
	
	
}
